var botao = document.querySelector(".navbar-menu_icon")
botao.addEventListener("click", aparecer)

function aparecer(){
    var menu = document.querySelector("#menujs");
    if (menu.style.height == "0px"){
        menu.style.height = "130px"

}else{
    menu.style.height = "0px"
    menu.style.height = "0px"

}

}


